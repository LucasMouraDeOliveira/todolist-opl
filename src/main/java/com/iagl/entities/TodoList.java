package com.iagl.entities;

public class TodoList {
	
	protected Integer id;
	
	protected String name;

	public TodoList() {
		
	}
	
	public Integer getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public void setName(String name) {
		this.name = name;
	}

}
